package net.javaguides.registration.model;

public class User {

	private String companyID;
	private String jobID;
	private String location;
	private String baseSalary;
	private String signOnBonus;
	private String annualBonus;
	private String otherPay;
	public String getCompanyID() {
		return companyID;
	}
	public void setCompanyID(String companyID) {
		this.companyID = companyID;
	}
	public String getJobID() {
		return jobID;
	}
	public void setJobID(String jobID) {
		this.jobID = jobID;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getBaseSalary() {
		return baseSalary;
	}
	public void setBaseSalary(String baseSalary) {
		this.baseSalary = baseSalary;
	}
	public String getSignOnBonus() {
		return signOnBonus;
	}
	public void setSignOnBonus(String signOnBonus) {
		this.signOnBonus = signOnBonus;
	}
	public String getAnnualBonus() {
		return annualBonus;
	}
	public void setAnnualBonus(String annualBonus) {
		this.annualBonus = annualBonus;
	}
	public String getOtherPay() {
		return otherPay;
	}
	public void setOtherPay(String otherPay) {
		this.otherPay = otherPay;
	}
	
}
